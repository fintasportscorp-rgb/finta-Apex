export function getMeasureTooltip(id: string, lang: 'fr' | 'en'): string | null {
  const t = MEASURE_TOOLTIPS[id]
  if (!t) return null
  return lang === 'fr' ? t.fr : t.en
}

export function getGestureLabel(id: string, gesture: string, lang: 'fr' | 'en'): string {
  if (lang === 'en') return GESTURE_EN[id] ?? gesture
  return gesture
}

export function getDisciplineLabel(discipline: string, lang: 'fr' | 'en'): string {
  if (lang === 'en') return DISCIPLINE_EN[discipline] ?? discipline
  return DISCIPLINE_FR[discipline] ?? discipline
}
